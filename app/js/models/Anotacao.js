class Anotacao {
    constructor(titulo, conteudo) {
        this.titulo = titulo;
        this.conteudo = conteudo;
    }
    getTitulo() {
        return this.titulo;
    }
    getConteudo() {
        return this.conteudo;
    }
    setTitulo(titulo) {
        this.titulo = titulo;
    }
    setConteudo(conteudo) {
        this.conteudo = conteudo;
    }
}
